<template>
  <div class="my-container w-100 h-100">
    <!--<div class="logo"></div>-->
    <bd_head
      :show_switch_type="true" :switch_type_text="offline_show?'返回登录':'离线虚机'" @switch_type="offline_show=!offline_show"
      :show_computer_setting="!computer_setting" @computer_setting="computer_setting=true"
      :show_back="computer_setting" @back="computer_setting=false"
      :show_shutdown="true"
    ></bd_head>

    <div v-show="!computer_setting && !offline_show">
      <div class="header w-100  mt-5">
        <h1 class="text-center">云桌面用户登录</h1>
      </div>

      <form class="h-50 form ml-auto mr-auto">
        <div class="input w-100">
          <input type="text" class="form-control w-100" placeholder="输入用户名"
                 :class="{'is-invalid':form.account.is_invalid}"
                 v-model="form.account.val">
          <div class="account"></div>
          <div class="invalid-feedback">
            {{form.account.help_text}}
          </div>
        </div>
        <div class="input w-100">
          <input id="password" type="text" class="form-control w-100" placeholder="输入密码"
                 :class="{'is-invalid':form.password.is_invalid}"
                 v-model="form.password.val" @keyup.enter="submit">
          <div class="password"></div>
          <div class="invalid-feedback">
            {{form.password.help_text}}
          </div>
        </div>
        <div class="input w-100" v-if="form.dynamicPass.is_use">
          <input type="text" class="form-control w-100" placeholder="输入动态密码"
                 :class="{'is-invalid':form.dynamicPass.is_invalid}"
                 v-model="form.dynamicPass.val" @keyup.enter="submit">
          <div class="password"></div>
          <div class="invalid-feedback">
            {{form.dynamicPass.help_text}}
          </div>
        </div>
        <div class="input" id="checkboxs">
          <div class="custom-control custom-checkbox">
            <input type="checkbox" class="custom-control-input" id="remember_pass" v-model="remember_pass">
            <label class="custom-control-label" for="remember_pass">记住密码</label>
          </div>
          <div class="custom-control custom-checkbox">
            <input type="checkbox" class="custom-control-input" id="auto_login" v-model="auto_login">
            <label class="custom-control-label" for="auto_login">自动登录</label>
          </div>
        </div>
        <button type="button" class="green_button btn btn-primary w-100" @click="submit">登录</button>
      </form>
    </div>
    <div v-show="computer_setting">
      <computer_setting :button_homes="true"></computer_setting>
    </div>
    <div v-show="offline_show" style="margin-top: 5rem">
      <offline></offline>
    </div>

    <bd_foot></bd_foot>
  </div>
</template>

<script>

  import bd_head from '@/common/bd_head';
  import bd_foot from '@/common/bd_foot';
  import computer_setting from '@/common/computer_setting';
  import offline from '@/components/VM/offline';

  export default {
    name: 'Login',
    components: {bd_foot, bd_head, computer_setting,offline},
    data() {
      return {
        offline_show: false,
        form: {
          account: {
            val: '',
            type: 'account',
            is_invalid: false,
            help_text: '账号不能为空',
          },
          password: {
            val: '',
            is_invalid: false,
            help_text: '密码不能为空',
          },
          dynamicPass: {
            is_use: false,
            val: '',
            is_invalid: false,
            help_text: '动态密码不能为空',
          },
        },
        remember_pass: false,
        auto_login: false,
        computer_setting: false,
      }
    },
    computed: {},
    watch: {
      auto_login(val) {
        if (val) {
          this.remember_pass = true;
        }
      }
    },
    methods: {
      shutdown() {
        const vm = this;
        vm.$ajax.get(`/api/operation_system/shutdown`)
          .then(resp => {
            // do nothing
          }).catch(error => {
          if (error && error.response && error.response.status == 401)
            return
          vm.$root.alert.fail.is_show = true;
          vm.$root.alert.fail.message = '关机失败';
        }).finally(() => {
          // vm.lan_data = vm.lans[0];
        });
      },
      submit() {
        const vm = this;
        for (let key in vm.form) {
          vm.form[key].is_invalid = false;
        }
        if (!vm.form.account.val || !vm.form.password.val) {
          if (!vm.form.account.val) {
            vm.form.account.is_invalid = true;
          }
          if (!vm.form.password.val) {
            vm.form.password.is_invalid = true;
          }
          if (!vm.form.dynamicPass.val) {
            vm.form.dynamicPass.is_invalid = true;
          }
          return
        }
        vm.$ajax.get(`/api/account/login`, {
          params: {
            account: vm.form.account.val,
            password: vm.form.password.val,
            dynamicPass: vm.form.dynamicPass.val,
            systemType: vm.utils.get_system_typenum(),
            remember_pass: vm.remember_pass,
            auto_login: vm.auto_login,
          }
        })
          .then(resp => {
            vm.conf.account = vm.form.account.val;
            vm.$router.push('/');
          }).catch(error => {
          if (error.response)
            vm.$root.alert.fail.message = error.response.data.content;
          vm.$root.alert.fail.is_show = true;
        }).finally(() => {
        });

        // vm.$ajax.get(`/api/common_api/api.login`, {
        //   params: {
        //     account: vm.form.account.val,
        //     password: vm.form.password.val,
        //     systemType: system_type,
        //   }
        // })
        //   .then(resp => {
        //     if (resp.data.errorinfo == 'success') {
        //       debugger;
        //       vm.$router.push('/');
        //     } else {
        //       vm.$root.alert.fail.message = resp.data.errorinfo;
        //       vm.$root.alert.fail.is_show = true;
        //     }
        //
        //   }).catch(error => {
        //   vm.$root.alert.fail.is_show = true;
        // }).finally(() => {
        //   // vm.lan_data = vm.lans[0];
        // });
      }
    },
    beforeCreate() {
      let vm = this;
      //
      vm.$ajax.get('/api/account/get_ready_data', {})
        .then(resp => {
          if (resp.data.content == 'not_ready') {

          }
          else {
            vm.form.account.val = resp.data.content.account;
            if (JSON.parse(resp.data.content.remember_pass)) {
              vm.form.password.val = resp.data.content.password;
              vm.remember_pass = JSON.parse(resp.data.content.remember_pass);
              vm.auto_login = JSON.parse(resp.data.content.auto_login);
            }
          }
        }).catch(error => {
        console.error(error.response.data.content);
        vm.$root.alert.fail.message = '获取账号信息失败，请直接登录';
        vm.$root.alert.fail.is_show = true;
      })
      vm.$ajax.get('/api/account/is_dynamic', {})
        .then(resp => {
          if (resp.data.content == 'true') {
            vm.form.dynamicPass.is_use = true;
          }
          else {
            vm.form.dynamicPass.is_use = false;
            if (vm.auto_login && vm.$root.can_auto_login) {
              vm.submit();
            }
          }
        }).catch(error => {
        vm.$root.alert.fail.message = error.response.data.content;
        vm.$root.alert.fail.is_show = true;
      })
    },
    mounted() {
      $('#password').attr('type', 'password');
    }
  }
</script>

